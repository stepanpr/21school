#include "../includes/libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*curr;
	t_list	*next;

	if (!lst || !del)
		return ;
	curr = *lst;
	next = *lst;
	while (next)
	{
		curr = next;
		next = next->next;
		del(curr->content);
		free(curr);
	}
	*lst = NULL;
}
