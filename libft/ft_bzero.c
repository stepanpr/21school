#include "libft.h"

void				ft_bzero(void *s, size_t n)
{
	char			*ptr;
	unsigned int	i;

	ptr = (char*)s;
	i = 0;
	while (i < n)
	{
		ptr[i] = '\0';
		i++;
	}
}
